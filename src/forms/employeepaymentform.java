    private void GetMaxID() {
        try {
            st = DataAccess.con.createStatement();
            rs = st.executeQuery("select ifnull(max(PaymentID+1),1) as id from employeepayment");
            if (rs.next()) {
                int id = rs.getInt(1);
                txtpid.setText(Integer.toString(id));
            }
            st.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

    }

    void search(int id) {
        try {
            String str = "SELECT PaymentID,employeeid, Dateofpayment, PaymentAmount FROM employeepayment WHERE (PaymentID =" + id + ")";
            PreparedStatement pst = DataAccess.con.prepareStatement(str);
            rs = pst.executeQuery();
            if (rs.next()) {
                txtpid.setText(( rs.getString("PaymentID")));
                txteid.setSelectedItem(rs.getString("employeeid"));
                txtdop.setDate((rs.getDate("Dateofpayment")));
                txtamt.setText((rs.getString("PaymentAmount")));

                //btnDelete.setEnabled(true);
                btnsave.setText("UPDATE");
            }
        } catch (NumberFormatException | SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

    }

    void Clear() {
        txtpid.setText("");
        txtamt.setText("");

        GetMaxID();
        btndel.setEnabled(false);
        btnsave.setText("Save");
    }

    private void BindTable() {
        try {
            String sql = " select employeepayment.PaymentID,employeemaster.employeeid,employeepayment.Dateofpayment,employeepayment.PaymentAmount from employeepayment inner join employeemaster on employeemaster.employeeid=employeepayment.employeeid";
            st = DataAccess.con.createStatement();
            rs = st.executeQuery(sql);
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

    }
