    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        if (txtpid.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter Payment ID");
            txtpid.setFocusable(true);
        }
        if (txteid.getSelectedItem().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter Employee ID");
            txteid.setFocusable(true);
        }
        if (txtamt.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter Amount");
            txtamt.setFocusable(true);
        }
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        pdate = sdf.format(txtdop.getDate());
        Item Item=(Item)txteid.getSelectedItem();
        int id=Item.getValue();
        try {

            if (btnsave.getText().equalsIgnoreCase("Save")) {
                st = DataAccess.con.createStatement();

                st.executeUpdate("insert into employeepayment(Dateofpayment,employeeid,PaymentAmount)values ('" + pdate+ "','" + id + "','" + txtamt.getText() + "')");

                JOptionPane.showMessageDialog(null, "Record Saved Sucessfully............", "Employee Master", JOptionPane.INFORMATION_MESSAGE);
                st.close();
                BindTable();
            } else {
                st = DataAccess.con.createStatement();

                st.executeUpdate("UPDATE employeepayment SET Dateofpayment='" + pdate + "',PaymentAmount='" + txtamt.getText() + "' WHERE(PaymentID="+ txtpid.getText() + ")");

                JOptionPane.showMessageDialog(null, "Record Updated Sucessfully.............", "Employee Master", JOptionPane.WARNING_MESSAGE);
                st.close();

                BindTable();
                btnsave.setText("Save");
            }
            //Clear();BindTable();BindSearch();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.toString(), "Producer Master", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnsaveActionPerformed

