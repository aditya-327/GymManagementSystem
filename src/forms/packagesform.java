private void GetMaxID() {
        try {
            st = DataAccess.con.createStatement();
            rs = st.executeQuery("select ifnull(max(PackageID+1),1) as id from packagemaster");
            if (rs.next()) {
                int id = rs.getInt(1);
                txtpid.setText(Integer.toString(id));
            }
            st.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

    }
    void search(int id) {
        try {
            String str = "SELECT PackageID, PackageName, PackageDescription, PackagePrice, Duration FROM packagemaster WHERE (PackageID =" + id + ")";
            PreparedStatement pst = DataAccess.con.prepareStatement(str);
            rs = pst.executeQuery();
            if (rs.next()) {
                txtpid.setText(String.valueOf(rs.getInt("PackageID")));
                txtpname.setText(rs.getString("PackageName"));
                txtpprice.setText((rs.getString("PackagePrice")));
                txtdes.setText((rs.getString("PackageDescription")));
                //txtd.setSelectedItem(rs.getString("Duration"));
                
                //btnDelete.setEnabled(true);
                btnsave.setText("UPDATE");
            }
        } catch (NumberFormatException | SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

    }
        void Clear() {
        txtpid.setText("");
        txtpname.setText("");
        txtpprice.setText("");
        txtdes.setText("");
       // txtd.setSelectedIndex(0);

        GetMaxID();
        btndel.setEnabled(false);
        btnsave.setText("Save");
    }
        private void BindTable() {
        try {
            String sql = "select PackageID,PackageName,PackageDescription,PackagePrice,Duration from packagemaster";
            st = DataAccess.con.createStatement();
            rs = st.executeQuery(sql);
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

    }
