    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        try {

            if (btnsave.getText().equalsIgnoreCase("Save")) {
                st = DataAccess.con.createStatement();

                st.executeUpdate("insert into packagemaster(PackageName,PackageDescription,PackagePrice,Duration)values ('" + txtpname.getText() + "','" + txtdes.getText() + "','" + txtpprice.getText() + "','" + jTextField1.getText()+ "')");

                JOptionPane.showMessageDialog(null, "Record Saved Sucessfully............", "Package Master", JOptionPane.INFORMATION_MESSAGE);
                st.close();
                BindTable();
            } else {
                st = DataAccess.con.createStatement();

                st.executeUpdate("UPDATE packagemaster SET PackageName='" + txtpname.getText() + "',PackageDescription='" + txtdes.getText() + "',PackagePrice ='" + txtpprice.getText() + "',Duration='" + jTextField1.getText() + "' WHERE (PackageId =" + txtpid.getText() + ")");
                JOptionPane.showMessageDialog(null, "Record Updated Sucessfully.............", "Package Master", JOptionPane.WARNING_MESSAGE);
                st.close();

                BindTable();
                btnsave.setText("Save");
            }
            //Clear();BindTable();BindSearch();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.toString(), "Package Master", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnsaveActionPerformed

