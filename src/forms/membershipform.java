    private void txtdojFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtdojFocusLost
        // TODO add your handling code here:
         String date = ((JTextField)txtdoj.getDateEditor().getUiComponent()).getText();
         String b[] = date.split("/");
         int day = Integer.parseInt(b[0]);
         int mn = Integer.parseInt(b[1]);
         int yr = Integer.parseInt(b[2]);
         LocalDate l =LocalDate.of(yr,mn,day);
         LocalDate c = LocalDate.now();
         int res = Period.between(l,c).getYears();
         if(res<15){
              JOptionPane.showMessageDialog(null, "You Are Under Age", "Membership Master", JOptionPane.INFORMATION_MESSAGE);
         }
    }//GEN-LAST:event_txtdojFocusLost

    private void txtmconKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtmconKeyPressed
        // TODO add your handling code here:
     
        String mobileno=txtmcon.getText();
        int length=mobileno.length();
        char c = evt.getKeyChar();
        if(evt.getKeyChar()>='0' && evt.getKeyChar()<='9'){
            if(length<10){
            txtmcon.setEditable(true);
            }
            else{
            txtmcon.setEditable(false);
            }
        }else{
            if(evt.getExtendedKeyCode()==KeyEvent.VK_BACK_SPACE ||evt.getExtendedKeyCode()==KeyEvent.VK_DELETE){
            txtmcon.setEditable(true);
            }else{
            txtmcon.setEditable(false);
            }
        }
    }//GEN-LAST:event_txtmconKeyPressed

