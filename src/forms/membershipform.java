        SimpleDateFormat fds = new SimpleDateFormat("yyyy-MM-dd");
        
        if(ContactNumber.matches("^[0-9] $")||ContactNumber.length()!=10)
        {
            JOptionPane.showMessageDialog(null, "Contact is incorrect!");
        }
        else
        {
            
        
        
        
        dob = fds.format(txtdate.getDate());
        try {

            if (btnsave.getText().equalsIgnoreCase("Save")) {
                st = DataAccess.con.createStatement();

                st.executeUpdate("insert into membershipmaster(membername,Doj,Dob,weight,height,PackageId,ContactNumber,Email,validity)values ('" + txtmname.getText() + "','" + doj + "','" + dob + "','" + cmdw.getValue() + "','" + cmdh.getValue() + "','" + cmbpid.getSelectedIndex() + "','" + txtmcon.getText() + "','" + txtemail.getText() + "','" + cmbval.getSelectedItem() + "')");

                JOptionPane.showMessageDialog(null, "Record Saved Sucessfully........", "membershipmaster", JOptionPane.INFORMATION_MESSAGE);
                st.close();
                BindTable();
            } else {
                st = DataAccess.con.createStatement();

                st.executeUpdate("UPDATE membershipmaster SET membername='" + txtmname.getText() + "',Doj='" + doj + "',Dob='" + dob + "',weight='" + cmdw.getValue() + "',height='" + cmdh.getValue() + "',ContactNumber='" + txtmcon.getText() + "',Email ='" + txtemail.getText() + "',validity='" + cmbval.getSelectedItem() + "' WHERE (memberid=" + txtmid.getText() + ")");
                JOptionPane.showMessageDialog(null, "Record Updated Sucessfully.............", "membershipmaster", JOptionPane.WARNING_MESSAGE);
                st.close();

                BindTable();
                btnsave.setText("Save");
            }
            //Clear();BindTable();BindSearch();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.toString(), "membershipmaster", JOptionPane.INFORMATION_MESSAGE);
        }
        }
