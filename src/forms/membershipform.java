    public void BindCombo() {
        try {
            String name = "";
            int itemID;
            boolean cnt = true;
            Vector comboboxitem = new Vector();
            st = DataAccess.con.createStatement();
            ResultSet rs = st.executeQuery("SELECT PackageID,PackageName FROM  packagemaster");
            comboboxitem.add(new Item(0, "Select Item"));
            while (rs.next()) {
                cnt = false;
                itemID = rs.getInt("PackageID");
                name = rs.getString("PackageName");
                comboboxitem.add(new Item(itemID, name));
            }
            if (cnt) {
                comboboxitem.add(new Item(0, "No Items Avilable"));
            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(comboboxitem);
            cmbpid.setModel(model);
            cmbpid.addActionListener((ActionListener) this);

        } catch (Exception ex) {
            //  JOptionPane.showMessageDialog(null, ex);
        }
    }
