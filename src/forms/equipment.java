    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
       
    
                try {

            if (btnsave.getText().equalsIgnoreCase("Save")) {
                st = DataAccess.con.createStatement();

                st.executeUpdate("insert into equipmentmaster(equipmentname,equipmenttype,equipmentcatagory,equipmentrate,eNumber)values ('" + eqname.getText() + "','" + cmbtype.getSelectedItem() + "','" + cmbCat.getSelectedItem() + "','" + txtrate.getText() +"', '" + jTextField1.getText() +"')");

                JOptionPane.showMessageDialog(null, "Record Saved Sucessfully............", "equipment master", JOptionPane.INFORMATION_MESSAGE);
                st.close();
                BindTable();
            } else {
                st = DataAccess.con.createStatement();

                st.executeUpdate("UPDATE equipmentmaster SET equipmentname='" + eqname.getText() + "',equipmenttype='" +  cmbtype.getSelectedItem() + "',equipmentcatagory='" + cmbCat.getSelectedItem() +"',equipmentrate='" + txtrate.getText() +"',eNumber='"+jTextField1.getText()+ "'WHERE (equipmentid =" + txteid.getText() + ")");
                JOptionPane.showMessageDialog(null, "Record Updated Sucessfully.............", "equipment master", JOptionPane.WARNING_MESSAGE);
                st.close();

                BindTable();
                btnsave.setText("Save");
            }
            //Clear();BindTable();BindSearch();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.toString(), "Producer Master", JOptionPane.INFORMATION_MESSAGE);
        }
        if(txteid.getText().equals("")){
        JOptionPane.showMessageDialog(null,"Enter Equipment ID");
        txteid.setFocusable(true);
        }if(eqname.getText().equals("")){
        JOptionPane.showMessageDialog(null,"Enter Equipment Name");
        eqname.setFocusable(true);
        }if(cmbtype.getSelectedItem().equals("Select Type")){
        JOptionPane.showMessageDialog(null,"Enter Equipment type");
        cmbtype.setFocusable(true);
        }
    }//GEN-LAST:event_btnsaveActionPerformed
