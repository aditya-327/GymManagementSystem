    private void GetMaxID() {
        try {
            st = DataAccess.con.createStatement();
            rs = st.executeQuery("select ifnull(max(equipmentid+1),1) as id from equipmentmaster");
            if (rs.next()) {
                int id = rs.getInt(1);
                txteid.setText(Integer.toString(id));
            }
            st.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

    }
    void search(int id) {
        try {
            String str = "SELECT  equipmentid,equipmentname, equipmenttype, equipmentcatagory,equipmentrate,eNumber FROM equipmentmaster WHERE (equipmentid =" + id + ")";
            PreparedStatement pst = DataAccess.con.prepareStatement(str);
            rs = pst.executeQuery();
            if (rs.next()) {
                txteid.setText(String.valueOf(rs.getInt("equipmentid")));
                eqname.setText(rs.getString("equipmentname"));
                cmbtype.setSelectedItem((rs.getString("equipmenttype")));
                cmbCat.setSelectedItem((rs.getString("equipmentcatagory")));
                jTextField1.setText((rs.getString("eNumber")));
                //btnDelete.setEnabled(true);
                txtrate.setText(String.valueOf(rs.getInt("equipmentrate")));
                btnsave.setText("UPDATE");
            }
        } catch (NumberFormatException | SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

    }
            private void BindTable() {
        try {
            String sql = "select equipmentid,equipmentname,equipmenttype, equipmentcatagory,equipmentrate,eNumber from equipmentmaster";
            st = DataAccess.con.createStatement();
            rs = st.executeQuery(sql);
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

    }
