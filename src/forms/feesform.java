private void GetMaxID() {
        try {
            st = DataAccess.con.createStatement();
            rs = st.executeQuery("select ifnull(max(FeeID+1),1) as id from feesmaster");
            if (rs.next()) {
                int id = rs.getInt(1);
                txtfid.setText(Integer.toString(id));
            }
            st.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

    }
 void search(int id) {
        try {
            String str = "SELECT FeeID,FeesDate,memberid,Amount,DueAmount,netamount,modeofpayment FROM feesmaster WHERE (FeeID =" + id + ")";
            PreparedStatement pst = DataAccess.con.prepareStatement(str);
            rs = pst.executeQuery();
            if (rs.next()) {
                txtfid.setText(String.valueOf(rs.getInt("FeeID")));
                cmbmid.setSelectedItem(rs.getString("memberid"));
                txtfamt.setText((rs.getString("Amount")));
               txtfdamt.setText((rs.getString("DueAmount")));
                txtnamt.setText(rs.getString("netamount"));
                txtfdate.setDate(rs.getDate("feesdate"));
                cmbmop.setSelectedItem(rs.getString("modeofpayment"));

                //btnDelete.setEnabled(true);
                btnsave.setText("UPDATE");
            }
        } catch (NumberFormatException | SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

    }
 
 
