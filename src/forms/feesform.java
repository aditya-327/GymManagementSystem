    private void BindTable() {
        try {
            String sql = "select feesmaster.FeeID,feesmaster.FeesDate,membershipmaster.membername ,membershipmaster.memberid,feesmaster.Amount,feesmaster.DueAmount,feesmaster.netamount,feesmaster.modeofpayment from feesmaster inner join membershipmaster on membershipmaster.memberid=feesmaster.FeeID";
            st = DataAccess.con.createStatement();
            rs = st.executeQuery(sql);
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));//memberid membername
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

    }
